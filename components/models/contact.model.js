import mongoose from "mongoose";

const contactSchema = mongoose.Schema({
    fname: {
        type: String,
        required: true
    },
    lname: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    phone: {
        type: String,
        required: true
    },
    subject: {
        type: String,
        required: true
    },
    message: {
        type: String,
        required: true
    },
},{
    timestamps:true
})

const Contact = mongoose.models.Contact || mongoose.model('Contact',contactSchema);

export default Contact;